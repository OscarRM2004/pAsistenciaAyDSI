    private void tbAreaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbAreaMouseClicked
        DefaultTableModel model = (DefaultTableModel) tbArea.getModel();
        int selectedRowIndex = tbArea.getSelectedRow();
        txtId.setText(model.getValueAt(selectedRowIndex, 0).toString());
        txtDescripcion.setText(model.getValueAt(selectedRowIndex, 1).toString());
    }//GEN-LAST:event_tbAreaMouseClicked

    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        AreaDTO p = new AreaDTO();
        AreaInterfaz per = new AreaImpl();
        p.setIdArea(Integer.parseInt(txtId.getText()));
        p.setDescripcion(txtDescripcion.getText());

        per.registrar(p);

        CargarTabla();
        limpiar();
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        AreaDTO p = new AreaDTO();
        AreaInterfaz per = new AreaImpl();
        p.setIdArea(Integer.parseInt(txtId.getText()));
        p.setDescripcion(txtDescripcion.getText());
        per.actualizar(p);
        CargarTabla();
        limpiar();
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        AreaDTO p = new AreaDTO();
        AreaInterfaz per = new AreaImpl();
        p.setIdArea(Integer.parseInt(txtId.getText()));
        per.eliminar(p);
        CargarTabla();
        limpiar();
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnRegresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegresarActionPerformed
        if (menuPrincipal != null) {
            menuPrincipal.limpiarPanelContenido();
        }
    }//GEN-LAST:event_btnRegresarActionPerformed


